import Foundation

// MARK: - Bool
// Init
var selected: Bool = false // Bool is a object type (a struct)

// MARK: Int
// init
var numTypeInt: Int = 125 // Int is a object type (a struct)

// MARK: Double
var numTypeDouble: Double = 12.52 // Double also is aobject type and is a struct

// MARK: String
var myName: String = "KhanhNVM" // String in Swift is a Object Type (a struct)

// MARK: Numeric coercion
let i = 10
let x = Double(i) // That's is coversion i has object type Int to object type Double
print(x) // Now if we print the variable's name will be has literal 10.0, a Double type
let y = 3.8
let j = Int(y) // Another conversion we will coercion, y has object type Double, it has coercion to object type Int
print(j)

